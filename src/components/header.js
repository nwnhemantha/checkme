class Header extends React.Component {
  state = {
    anchorEl: null,
    mobileMoreAnchorEl: null,
    isLogged: false,
    avatar: null
  };

  componentDidMount(){
    if(localStorage.getItem("loggedUser")){
      let user = JSON.parse(localStorage.getItem("loggedUser"));
      this.setState({avatar: user.picture, isLogged: true})
    }
  
  }

  componentWillReceiveProps(np){
    let user = JSON.parse(localStorage.getItem("loggedUser"));
      if(user){
      this.setState({avatar: user.picture, isLogged: true})
    }else {
      this.setState({isLogged: false})
    }

    
  }

  handleProfileMenuOpen = event => {
    this.setState({ anchorEl: event.currentTarget });
  };

  handleMenuClose = () => {
    this.setState({ anchorEl: null });
    this.handleMobileMenuClose();
  };

  handleMobileMenuOpen = event => {
    this.setState({ mobileMoreAnchorEl: event.currentTarget });
  };

  handleMobileMenuClose = () => {
    this.setState({ mobileMoreAnchorEl: null });
  };

  redirecHome(){
    window.location.replace('/')
  }

  logout = () => {
    localStorage.removeItem('loggedUser');
    this.setState({anchorEl: null});
    this.props.logout();
    window.location.replace('/')
    
  }

  render() {
    const { anchorEl, mobileMoreAnchorEl } = this.state;
    const { classes } = this.props;
    const isMenuOpen = Boolean(anchorEl);
    const isMobileMenuOpen = Boolean(mobileMoreAnchorEl);

    const renderMenu = (
      <Menu
        anchorEl={anchorEl}
        anchorOrigin={{ vertical: 'top', horizontal: 'right' }}
        transformOrigin={{ vertical: 'top', horizontal: 'right' }}
        open={isMenuOpen}
        onClose={this.handleMenuClose}
      >
        <MenuItem onClick={this.logout}>Log out</MenuItem>
      </Menu>
    );

    const renderMobileMenu = (
      <Menu
        anchorEl={mobileMoreAnchorEl}
        anchorOrigin={{ vertical: 'top', horizontal: 'right' }}
        transformOrigin={{ vertical: 'top', horizontal: 'right' }}
        open={isMobileMenuOpen}
        onClose={this.handleMobileMenuClose}
      >
        <MenuItem onClick={this.handleProfileMenuOpen}>
          <IconButton color="inherit">
          {
            !this.state.isLogged && <AccountCircle />
          }
          {  this.state.isLogged &&
            <Avatar alt="Remy Sharp" src={this.state.avatar} />
          }
          </IconButton>
        </MenuItem>
      </Menu>
    );

    return (
      <div className={classes.root}>
        <AppBar position="fixed">
          <Toolbar>
            <Typography className={classes.title} variant="h6" color="inherit" noWrap onClick={this.redirecHome}>
              CHECKME.LK
            </Typography>
            <div className={classes.search}>
              <div className={classes.searchIcon}>
                <SearchIcon />
              </div>
              <InputBase
                placeholder="Searchâ€¦"
                classes={{
                  root: classes.inputRoot,
                  input: classes.inputInput,
                }}
              />
            </div>
            <div className={classes.grow} />
            <div className={classes.sectionDesktop}>
              {/* <IconButton color="inherit">
                <Badge badgeContent={4} color="secondary">
                  <MailIcon />
                </Badge>
              </IconButton>
              <IconButton color="inherit">
                <Badge badgeContent={17} color="secondary">
                  <NotificationsIcon />
                </Badge>
              </IconButton> */}
              <IconButton
                aria-owns={isMenuOpen ? 'material-appbar' : undefined}
                aria-haspopup="true"
                onClick={this.handleProfileMenuOpen}
                color="inherit"
              >
                {
                  !this.state.isLogged && <AccountCircle />
                }
                {  this.state.isLogged &&
                  <Avatar alt="Remy Sharp" src={this.state.avatar} />
                }
              </IconButton>
            </div>
            <div className={classes.sectionMobile}>
              <IconButton aria-haspopup="true" onClick={this.handleMobileMenuOpen} color="inherit">
                <MoreIcon />
              </IconButton>
            </div>
          </Toolbar>
        </AppBar>
        { renderMenu}
        { renderMobileMenu}
      </div>
    );
  }
}

Header.propTypes = {
  classes: PropTypes.object.isRequired,
};


const mapStateToProps = state => ({
  ...state
})

const mapDispatchToProps = {
  logout
}


export default connect(mapStateToProps, mapDispatchToProps) (withStyles(styles)(Header));
